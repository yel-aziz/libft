#include"libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	lenght;
	size_t	i;
	char	*p;

	lenght = n;
	p = (char *)s;
	i = 0;
	while (i < lenght)
	{		
		p[i] = 0;
		i++;
	}
}

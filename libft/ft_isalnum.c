#include"libft.h"

int	ft_isalnum(int c)
{
	if (!c)
		return (0);
	if ((c >= '0' && c <= '9')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
